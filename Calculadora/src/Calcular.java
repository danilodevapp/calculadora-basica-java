public class Calcular {

    public double Numero1, Numero2;

    //Método que permite a soma de dois números passados por parâmetros com retorno double
    public double Somar (double numero1, double numero2){
        numero1 = Numero1;
        numero2 = Numero2;
        return numero1 + numero2;
    }

    //Método que permite a subtração de dois números passados por parâmetros com retorno double
    public double Subtrair (double numero1, double numero2){
        numero1 = Numero1;
        numero2 = Numero2;
        return numero1 - numero2;
    }

    //Método que permite a multiplicação de dois números passados por parâmetros com retorno double
    public double Multiplicar (double numero1, double numero2){
        numero1 = Numero1;
        numero2 = Numero2;
        return numero1 * numero2;
    }

    //Método que permite a divisão de dois números passados por parâmetros com retorno double
    public double Dividir (double numero1, double numero2){
        numero1 = Numero1;
        numero2 = Numero2;
        return numero1 / numero2;
    }
}

